package de.presti.ree6.webinterface.bot.version;

/**
 * Class with the different Bot States.
 */
public enum BotState {

    /**
     * State when the Bot starts.
     */
    INIT,

    /**
     * State when the Bot has started.
     */
    STARTED,

    /**
     * State when the Bot is shutting down.
     */
    STOPPED

}
